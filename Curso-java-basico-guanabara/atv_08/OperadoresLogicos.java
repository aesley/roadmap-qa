package aesley.java_basico.atividades_guanabara.atv_08;

/* Conteúdo operadores lógicos.
 * Segue os operadores lógicos:
 * maior >, menor <, maior igual >=, menor igual <=, igualdade ==, diferente !=.
 */
public class OperadoresLogicos {
    public static void main(String[] args) {
        int n1, n2, r;
        n1 = 4;
        n2 = 8;
        r = (n1>n2)?0:1; //se n1>n2 ele mostra 0 se não 1
        System.out.println(r);
    }
}
